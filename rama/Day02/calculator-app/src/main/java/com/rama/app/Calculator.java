package com.rama.app;

public class Calculator {
    public int addition(int  a,int b){
        return a+b;
    }
    public double substraction(float  a,float b){
        return a-b;
    }
    public double multiplication(float  a,float b){
        return a*b;
    }
    public double division(float  a,float b){
        return a/b;
    }
}
